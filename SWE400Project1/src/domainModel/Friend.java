package domainModel;

public class Friend {

	protected int friendID;

	/**
	 * Gets the ID of the friend being added.
	 * @return the friends ID
	 */
	public int getFriendID() {
		return friendID;
	}

	/**
	 * Sets the friends ID who is being added.
	 * @param friendID
	 */
	public void setFriendID(int friendID) {
		this.friendID = friendID;
	}

}
